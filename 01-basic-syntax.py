edad = 25 # Entero (int)
altura = 1.75 # Flotante (float)
es_estudiante = True # Booleano (bool)
## Tipos básicos:
### int, float, str, bool, None
### Colecciones: list, tuple, dict, set
## Mutabilidad:
### Inmutables: int, str, tuble, bool
### Mutables: list, dict, set


